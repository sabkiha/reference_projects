        <p>
          From 10 until 14 July 2018, on the occasion of the 100th anniversary of the dissolution of the Austro-Hungarian Empire, border scholars from around the globe will come together in Vienna in Budapest to discuss borders. This world academic conference on borders will become a site of scientific investigation itself, where 100 citizen scientists (bachelor students in the humanities from border regions throughout the ex-Habsburg area) will meet 100 border scholars as equals for a cross-disciplinary (border/citizen science) experiment.  
        </p>
        <p>This Citizen Science project asks:</p>
        <ol>
            <li>What do borders mean to border scholars?</li>
            <li>What do borders mean to young adults from the (ex-) Habsburg area?</li>
            <li>What new knowledge does a global encounter between citizen scientists and border scholars reveal?</li>
        </ol>
        <p>The Experiment consists of two aspects:</p>
        <ol  type="a">
            <li>It will gather 100 face-to-face dialogues about the meaning of borders.</li>
            <li>It will also host a global digital café for 100 working days, where extracts from the 100 talks will be posted so that people can comment on them. The online page will demonstrate how scientific knowledge on the global meaning(s) of borders is generated.</li>
        </ol>
        <p>
            A special focus will be put on potentially common narratives among citizen scientists in articulating the meaning of borders. How is the border verbally drawn and how did it evolve during the further global conversations? The project aims to offer a solid empirical data basis for future research in border studies. It also aims to draw attention to the limits of public expertise in the knowledge and information society of the 21st century, as well as the limits of the production and consumption of science.
        </p>
